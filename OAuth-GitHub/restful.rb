# A RESTful API
# An API, or application programming interface, is kind of like a coding contract: it specifies the ways a program can interact with an application. 
# For example, if you want to write a program that reads and analyzes data from Twitter, you'd need to use the Twitter API, 
# which would specify the process for authentication, important URLs, classes, methods, and so on.

# For an API or web service to be RESTful, it must do the following:

# Separate the client from the server
# Not hold state between requests (meaning that all the information necessary to respond to a request is available in each individual request; 
# no data, or state, is held by the server from request to request)
# Use HTTP and HTTP methods (as explained in the next section).

require 'open-uri'

kittens = open('http://placekitten.com/200/300')

f = File.open('kittens.jpg', 'w')
kittens.each do |kitten|
  f.write(kitten)
end

f.close





































