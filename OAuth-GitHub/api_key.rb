Authentication & API Keys
Many APIs require an API key. Just as a real-world key allows you to access something, 
an API key grants you access to a particular API. Moreover, an API key identifies you to the API, which helps the API provider keep track of how their service is used and prevent unauthorized or malicious activity.

Some APIs require authentication using a protocol called OAuth. We won't get into the details, 
but if you've ever been redirected to a page asking for permission to link an application with your account, you've probably used OAuth.

